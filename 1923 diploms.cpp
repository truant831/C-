#include <iostream>
#include <algorithm>

using namespace std;

bool canFitDiplomas(long long S, long long w, long long h, long long n) {
    long long diplomasPerRow = S / w;
    long long rows = S / h;
    return (diplomasPerRow * rows) >= n;
}

long long findMinSquareSide(long long w, long long h, long long n) {
    long long left = max(w, h);
    long long right = max(w, h) * n;
    long long minSide = right;

    while (left <= right) {
        long long mid = left + (right - left) / 2;
        if (canFitDiplomas(mid, w, h, n)) {
            minSide = mid;
            right = mid - 1;  // Try for a smaller side
        } else {
            left = mid + 1;  // Try for a larger side
        }
    }
    return minSide;
}

